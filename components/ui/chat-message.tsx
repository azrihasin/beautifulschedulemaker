function ToolCall({
  toolInvocations,
}: Pick<ChatMessageProps, "toolInvocations">) {
  if (!toolInvocations?.length) return null

  const handleToolExecution = async (toolName: string, args: Record<string, unknown>) => {
    // This function can be used to update the UI or trigger additional actions
    console.log(`Tool ${toolName} executed with args:`, args)
  }

  return (
    <div className="flex flex-col items-start gap-2">
      {toolInvocations.map((invocation, index) => {
        const isCancelled =
          invocation.state === "result" &&
          invocation.result.__cancelled === true

        // Convert AI SDK tool invocation to Tool component format
        const toolPart: ToolPart = {
          type: invocation.toolName,
          state: isCancelled 
            ? "output-error"
            : invocation.state === "partial-call" || invocation.state === "call"
            ? "input-streaming"
            : invocation.state === "result"
            ? "output-available"
            : "input-available",
          input: invocation.state === "call" ? (invocation as any).args : undefined,
          output: invocation.state === "result" ? invocation.result : undefined,
          toolCallId: `${invocation.toolName}-${index}`,
          errorText: isCancelled ? "Tool execution was cancelled" : undefined,
          onExecute: handleToolExecution
        }

        return (
          <div className="my-2" key={index}>
            <Tool
              toolPart={toolPart}
              defaultOpen={false}
              className="w-full max-w-2xl transition-all duration-200 hover:shadow-md"
            />
          </div>
        )
      })}
    </div>
  )
}
